#include<unistd.h>

void	ft_print_comb(void)
{
	int	n1;
	int	n2;
	int	n3;

	n1 = 47;
	while (n1++ <= 56)
	{
		n2 = n1;
		while (n2++ <= 56)
		{
			n3 = n2;
			while (n3++ <= 56)
			{
				if (!((n1 == 48) && (n2 == 49) && (n3 == 50)))
					write(1, ", ", 2);
				write(1, &n1, 1);
				write(1, &n2, 1);
				write(1, &n3, 1);
			}
		}
	}
}
